def _compute_scope_import_matrix(
    scopes: dict,
    graph_edges: Dict[str, List[str]],
) -> Dict[str, Dict[str, int]]:
    """Compute which scopes import from which other scopes.

    Returns: {source_scope: {target_scope: import_count}}
    """
    # Build file→scope mapping
    file_to_scope = {}
    for scope_name, scope_data in scopes.items():
        for f in scope_data.get("files", []):
            file_to_scope[f] = scope_name

    matrix: Dict[str, Dict[str, int]] = {}
    for source_file, targets in graph_edges.items():
        source_scope = file_to_scope.get(source_file, "")
        if not source_scope:
            continue
        if source_scope not in matrix:
            matrix[source_scope] = {}
        for target_file in targets:
            target_scope = file_to_scope.get(target_file, "")
            if target_scope and target_scope != source_scope:
                matrix[source_scope][target_scope] = matrix[source_scope].get(target_scope, 0) + 1

    return matrix


def _load_scope_data(root: str) -> dict:
    """Load scope data from .scope files."""
    try:
        from ..discovery import find_all_scopes
        from ..parser import parse_scope_file
        from ..intent import load_conventions

        scopes = {}
        for sf in find_all_scopes(root):
            try:
                config = parse_scope_file(sf)
                name = os.path.basename(os.path.dirname(config.path)) or "root"
                scopes[name] = {
                    "files": config.includes,
                    "anti_patterns": config.anti_patterns if hasattr(config, "anti_patterns") else [],
                    "context": config.context_str,
                }
            except Exception:
                continue

        # Merge conventions
        conventions = load_conventions(root)
        if conventions:
            for conv in conventions:
                # Assign convention to most relevant scope
                for scope_name in scopes:
                    if scope_name not in scopes.get(scope_name, {}).get("conventions", []):
                        if "conventions" not in scopes.get(scope_name, {}):
                            scopes[scope_name]["conventions"] = []
                        scopes[scope_name]["conventions"].append({
                            "name": conv.name,
                            "compliance": conv.compliance,
                            "description": conv.description,
                            "rules": conv.rules,
                        })
                        break  # Assign to first scope

        return scopes
    except Exception:
        return {}


def _load_graph_edges(root: str) -> Dict[str, List[str]]:
    """Load import edges from cached graph."""
    try:
        from ..storage.cache import load_cached_graph_hubs
        hubs = load_cached_graph_hubs(root)
        # Reconstruct edges from hubs (imported_by → reverse edges)
        edges: Dict[str, List[str]] = {}
        for path, hub in hubs.items():
            imported_by = hub.get("imported_by_files", [])
            if isinstance(imported_by, list):
                for importer in imported_by:
                    if importer not in edges:
                        edges[importer] = []
                    edges[importer].append(path)
        return edges
    except Exception:
        return {}


def _load_npmi_index(root: str) -> Dict[str, Dict[str, float]]:
    """Load NPMI co-change index."""
    path = Path(root) / ".dotscope" / "cache" / "npmi_index.json"
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {}
