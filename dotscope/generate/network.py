"""NETWORK_MAP.md renderer.

Renders the cross-language network topology: provider endpoints,
consumer bindings, unmatched consumers, and multi-hop flow chains.
Flow chains computed via BFS with cycle detection.
"""

import json
import os
from pathlib import Path
from typing import Dict, List, Optional, Set

from .models import GenerateConfig, GeneratedArtifact


def render_network_map(
    root: str,
    config: GenerateConfig,
    timestamp: str,
    commit: str,
) -> GeneratedArtifact:
    """Render NETWORK_MAP.md."""
    network_edges = _load_network_edges(root)
    confidence = _load_confidence(root)
    endpoints = _load_endpoints(root)
    consumers = _load_consumers(root)

    lines = [f"# Network Map\n"]
    lines.append(f"Generated by dotscope · {timestamp} · commit {commit}\n")

    stats = {"endpoints": 0, "bindings": 0, "unmatched": 0, "flows": 0}

    # Provider Endpoints table
    endpoint_list = _build_endpoint_list(endpoints, network_edges)
    if endpoint_list:
        lines.append("\n## Provider Endpoints\n")
        lines.append("| Endpoint | Method | Handler | Consumers |")
        lines.append("|----------|--------|---------|-----------|")
        # Sort by consumer count descending, then path
        endpoint_list.sort(key=lambda e: (-e["consumers"], e["path"]))
        for ep in endpoint_list:
            lines.append(
                f"| `{ep['path']}` | {ep['method']} | "
                f"`{ep['handler']}` | {ep['consumers']} |"
            )
            stats["endpoints"] += 1

    # Consumer Bindings table
    bindings = _build_bindings(network_edges, confidence)
    if bindings:
        lines.append("\n## Consumer Bindings\n")
        lines.append("| Consumer | Provider | Confidence | Type |")
        lines.append("|----------|----------|------------|------|")
        # Sort by confidence descending, then consumer path
        bindings.sort(key=lambda b: (-b["confidence"], b["consumer"]))
        for b in bindings:
            conf_type = _confidence_label(b["confidence"])
            lines.append(
                f"| `{b['consumer']}` → `{b['method']} {b['path']}` | "
                f"`{b['handler']}` | {b['confidence']:.1f} | {conf_type} |"
            )
            stats["bindings"] += 1

    # Unmatched Consumers
    all_matched = {b["consumer"] for b in bindings}
    unmatched = []
    for path, consumer_list in consumers.items():
        for cons in consumer_list:
            consumer_key = f"{path}"
            if consumer_key not in all_matched:
                unmatched.append({
                    "consumer": consumer_key,
                    "raw_path": cons.get("raw_path", ""),
                    "method": cons.get("method", "GET"),
                })

    if unmatched:
        lines.append("\n## Unmatched Consumers\n")
        lines.append("| Consumer | Raw Path | Reason |")
        lines.append("|----------|----------|--------|")
        unmatched.sort(key=lambda u: u["consumer"])
        for u in unmatched:
            lines.append(
                f"| `{u['consumer']}` | `{u['method']} {u['raw_path']}` | "
                f"No provider matched above 0.5 confidence |"
            )
            stats["unmatched"] += 1

    # Flow Chains (3+ hops only)
    chains = _compute_flow_chains(network_edges, endpoints)
    long_chains = [c for c in chains if len(c) >= 3]
    if long_chains:
        lines.append("\n## Request Flows\n")
        # Sort by depth descending, then entry path
        long_chains.sort(key=lambda c: (-len(c), c[0].get("path", "")))
        for chain in long_chains:
            lines.append("```")
            for i, hop in enumerate(chain):
                indent = "  " * i
                handler = hop.get("handler", "")
                path = hop.get("path", "")
                conf = hop.get("confidence", 0)
                if hop.get("cycle"):
                    lines.append(f"{indent}→ [cycle → {handler}]")
                elif i == 0:
                    lines.append(f"[{hop.get('consumer', '')}] → {hop.get('method', '')} {path}")
                else:
                    conf_str = f", confidence {conf:.1f}" if conf else ""
                    lines.append(f"{indent}→ {handler} ({hop.get('file', '')})")
            lines.append("```\n")
            stats["flows"] += 1

    content = "\n".join(lines)

    return GeneratedArtifact(
        name="network_map",
        content=content,
        file_name="NETWORK_MAP.md",
        data_sources=["network_edges.json", "network_confidence.json"],
        stats=stats,
    )


