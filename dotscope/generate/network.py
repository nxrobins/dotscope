# Builders
# ---------------------------------------------------------------------------

def _build_endpoint_list(
    endpoints: Dict[str, list],
    network_edges: Dict[str, Dict[str, list]],
) -> list:
    """Build a flat list of endpoints with consumer counts."""
    result = []
    seen_handlers = set()

    for file_path, ep_list in sorted(endpoints.items()):
        for ep in ep_list:
            handler = ep.get("handler", "")
            if handler in seen_handlers:
                continue
            seen_handlers.add(handler)

            consumer_count = len(network_edges.get(file_path, {}))
            result.append({
                "path": ep.get("path", ""),
                "method": ep.get("method", "ALL"),
                "handler": f"{file_path}:{handler}" if file_path else handler,
                "consumers": consumer_count,
            })

    return result


def _build_bindings(
    network_edges: Dict[str, Dict[str, list]],
    confidence: Dict,
) -> list:
    """Build consumer→provider binding list with confidence."""
    bindings = []

    for provider, consumers in sorted(network_edges.items()):
        for consumer, ep_list in sorted(consumers.items()):
            for ep in ep_list:
                # Look up confidence
                conf_key = f"{provider}|{consumer}"
                conf = confidence.get(conf_key, 1.0)
                if isinstance(conf, dict):
                    conf = 1.0

                handler = ep.get("handler", "") if isinstance(ep, dict) else getattr(ep, "handler_name", "")
                path = ep.get("path", "") if isinstance(ep, dict) else getattr(ep, "raw_path", "")
                method = ep.get("method", "") if isinstance(ep, dict) else getattr(ep, "method", "")

                bindings.append({
                    "consumer": consumer,
                    "provider": provider,
                    "handler": handler,
                    "path": path,
                    "method": method,
                    "confidence": float(conf),
                })

    return bindings


def _compute_flow_chains(
    network_edges: Dict[str, Dict[str, list]],
    endpoints: Dict[str, list],
) -> list:
    """Compute multi-hop request paths via BFS with cycle detection."""
    # Build handler→file mapping
    handler_to_file = {}
    for file_path, ep_list in endpoints.items():
        for ep in ep_list:
            handler = ep.get("handler", "")
            if handler:
                handler_to_file[handler] = file_path

    chains = []

    for provider_file, consumers in network_edges.items():
        for consumer_file, ep_list in consumers.items():
            for ep in ep_list:
                handler = ep.get("handler", "") if isinstance(ep, dict) else ""
                if not handler:
                    continue

                # BFS from this handler
                chain = [{"consumer": consumer_file, "method": ep.get("method", ""), "path": ep.get("path", ""), "handler": handler, "file": provider_file}]
                visited: Set[str] = {handler}

                current_file = provider_file
                while True:
                    # Check if current_file has outgoing network edges
                    next_hops = network_edges.get(current_file, {})
                    found_next = False
                    for next_consumer, next_eps in sorted(next_hops.items()):
                        for next_ep in next_eps:
                            next_handler = next_ep.get("handler", "") if isinstance(next_ep, dict) else ""
                            if not next_handler:
                                continue
                            if next_handler in visited:
                                chain.append({"handler": next_handler, "cycle": True})
                                found_next = False
                                break
                            visited.add(next_handler)
                            next_file = handler_to_file.get(next_handler, next_consumer)
                            chain.append({"handler": next_handler, "file": next_file, "path": next_ep.get("path", "")})
                            current_file = next_file
                            found_next = True
                            break
                        if found_next or chain[-1].get("cycle"):
                            break

                    if not found_next:
                        break

                if len(chain) >= 2:
                    chains.append(chain)

    return chains


def _confidence_label(conf: float) -> str:
    if conf >= 1.0:
        return "exact"
    elif conf >= 0.8:
        return "suffix"
    else:
        return "semantic"


