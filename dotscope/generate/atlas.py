def _load_npmi_index(root: str) -> Dict[str, Dict[str, float]]:
    path = Path(root) / ".dotscope" / "cache" / "npmi_index.json"
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {}


def _load_file_histories(root: str) -> Dict[str, dict]:
    try:
        from ..storage.cache import load_cached_history
        history = load_cached_history(root)
        if not history:
            return {}
        result = {}
        for fh in getattr(history, "file_histories", {}).values():
            result[fh.path] = {
                "stability": getattr(fh, "stability", ""),
                "last_modified": getattr(fh, "last_modified", 0),
                "commit_count": getattr(fh, "commit_count", 0),
                "recent_commits": getattr(fh, "commit_count", 0),
            }
        return result
    except Exception:
        return {}


def _load_graph_hubs(root: str) -> Dict[str, dict]:
    try:
        from ..storage.cache import load_cached_graph_hubs
        return load_cached_graph_hubs(root)
    except Exception:
        return {}


def _load_import_edges(root: str) -> Dict[str, List[str]]:
    try:
        from ..storage.cache import load_cached_graph_hubs
        hubs = load_cached_graph_hubs(root)
        edges: Dict[str, List[str]] = {}
        for path, hub in hubs.items():
            imported_by = hub.get("imported_by_files", [])
            if isinstance(imported_by, list):
                for importer in imported_by:
                    if importer not in edges:
                        edges[importer] = []
                    edges[importer].append(path)
        return edges
    except Exception:
        return {}


def _load_network_edges(root: str) -> Dict[str, Dict[str, list]]:
    try:
        from ..storage.cache import load_cached_network_edges
        return load_cached_network_edges(root)
    except Exception:
        return {}


def _build_file_scope_index(root: str) -> Dict[str, str]:
    try:
        from ..discovery import find_all_scopes
        from ..parser import parse_scope_file
        index = {}
        for sf in find_all_scopes(root):
            try:
                config = parse_scope_file(sf)
                name = os.path.basename(os.path.dirname(config.path)) or "root"
                for inc in config.includes:
                    index[inc] = name
            except Exception:
                continue
        return index
    except Exception:
        return {}
